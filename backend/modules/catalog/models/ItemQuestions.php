                    'title' => 'Основное',
                    'icon' => 'suitcase',
                    'options' => [],
                    'fields' => $fields
                ]
            ]
        ];
        return $result;
    }
    public function afterSave($insert, $changedAttributes)
    {
        $sql_count_reviews=new Expression(
            '(select COUNT(*) from `item_questions` WHERE `item_questions`.`item_id`=:item_id AND `item_questions`.isVisible=1 group by `item_questions`.`item_id`)',
            [':item_id'=>$this->item_id]
        );
        Items::updateAll(['count_questions'=>$sql_count_reviews], ['id' => $this->item_id]);
        parent::afterSave($insert, $changedAttributes); // TODO: Change the autogenerated stub
    }
}
